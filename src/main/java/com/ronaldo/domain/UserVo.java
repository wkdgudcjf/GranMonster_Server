package com.ronaldo.domain;

public class UserVo {
    private Long pkn;
    private String name;
    private String id;
	public Long getPkn() {
		return pkn;
	}
	public void setPkn(Long pkn) {
		this.pkn = pkn;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
}
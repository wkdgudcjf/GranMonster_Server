package com.ronaldo.vo;

public class ReceiveCompanyVO {
	String companyName;
	int companyID;
	boolean companyEnable;
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public int getCompanyID() {
		return companyID;
	}
	public void setCompanyID(int companyID) {
		this.companyID = companyID;
	}
	public boolean isCompanyEnable() {
		return companyEnable;
	}
	public void setCompanyEnable(boolean companyEnable) {
		this.companyEnable = companyEnable;
	}
}
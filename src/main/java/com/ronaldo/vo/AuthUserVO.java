package com.ronaldo.vo;

public class AuthUserVO {
    private String id;
    private String password;
    private String email;
    private String inputNumberCheck;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getInputNumberCheck() {
		return inputNumberCheck;
	}
	public void setInputNumberCheck(String inputNumberCheck) {
		this.inputNumberCheck = inputNumberCheck;
	}
	
}